const deposit = async () => {
	if (dDamount.getValue() < 0.01){
		alert("AMOUNT SHOULD BE GREATER THAN 0.01 ETH!");
	} else{
		$$("win1").show();
		let data = await fetch('https://min-api.cryptocompare.com/data/pricemulti?fsyms=ETH&tsyms=USD').then(response => response.json())
  		console.log(data["ETH"]["USD"])
		const transaction = await AmazonMPContract.deposit(parseInt(data["ETH"]["USD"]), { value: ethers.utils.parseEther(dDamount.getValue()) });
		await transaction.wait();
		$$("win1").hide();
		main();
	}
}

const withdraw = async() => {
	if (dWamount.getValue() <= 25){
		alert("MINIMUM WITHDRAWL AMOUNT 25 USD!");
	} else{
		$$("win1").show();
		let data = await fetch('https://min-api.cryptocompare.com/data/pricemulti?fsyms=ETH&tsyms=USD').then(response => response.json())
  		console.log(data["ETH"]["USD"])
		let cBalance = await AmazonMPContract.getBalance();
		const approveTrans = await AmazonUSD.approve(Amazon_Contract_Address, cBalance);
		await approveTrans.wait();
		const transaction = await AmazonMPContract.withdraw(parseInt(data["ETH"]["USD"]), dWamount.getValue());
		await transaction.wait();
		$$("win1").hide();
		main();
	}
}

